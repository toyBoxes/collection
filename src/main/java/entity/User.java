package entity;

import org.springframework.stereotype.Component;


public class User {
    private String name;
    private String age="0";

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
